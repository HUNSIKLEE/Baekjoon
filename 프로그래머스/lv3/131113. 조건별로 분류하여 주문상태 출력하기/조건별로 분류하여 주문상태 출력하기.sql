SELECT ORDER_ID, PRODUCT_ID, DATE_FORMAT(OUT_DATE, '%Y-%m-%d') OUT_DATE, 
CASE 
    WHEN DATE(OUT_DATE) between '2022-01-01' and '2022-05-01' THEN '출고완료' 
    WHEN DATE(OUT_DATE) between '2022-05-02' and '2022-12-31' THEN '출고대기' 
    ELSE '출고미정' 
    END 출고여부
FROM FOOD_ORDER
ORDER BY ORDER_ID